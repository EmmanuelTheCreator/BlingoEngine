using BlingoEngine.IO.Data.DTO.Members;

namespace BlingoEngine.Director.Core.Projects;

/// <summary>
/// Generates C# source files from a movie state.
/// </summary>
public class DirectorMovieCodeGenerator
{
    private const string _header = "// <auto-generated>\n// This code was generated by BlingoEngine.Director C#.\n// </auto-generated>\n";

    public void Generate(BlingoMovieDTO movieDto, string setupFolder)
    {
        var dir = setupFolder;
        if (!Directory.Exists(dir))
            Directory.CreateDirectory(dir);

        foreach (var cast in movieDto.Casts)
        {
            var code = GenerateCastClass(cast);
            File.WriteAllText(Path.Combine(dir, Sanitize(cast.Name) + "Cast.cs"), code);
        }

        var scoreCode = GenerateScoreClass(movieDto);
        File.WriteAllText(Path.Combine(dir, "ScoreBuilder.cs"), scoreCode);

        var movieCode = GenerateMovieClass(movieDto);
        File.WriteAllText(Path.Combine(dir, "MovieBuilder.cs"), movieCode);
    }

    public virtual string GenerateCastClass(BlingoCastDTO cast)
    {
        var sb = new StringBuilder();
        sb.Append(_header);
        sb.AppendLine("using BlingoEngine.Casts;");
        sb.AppendLine("using BlingoEngine.Core;");
        sb.AppendLine("using BlingoEngine.Members;");
        sb.AppendLine("using BlingoEngine.Bitmaps;");
        sb.AppendLine("using BlingoEngine.Sounds;");
        sb.AppendLine("using BlingoEngine.FilmLoops;");
        sb.AppendLine("using BlingoEngine.Texts;");
        sb.AppendLine("using BlingoEngine.Shapes;");
        sb.AppendLine("using BlingoEngine.Scripts;");
        sb.AppendLine("using BlingoEngine.ColorPalettes;");
        sb.AppendLine("using BlingoEngine.Transitions;");
        sb.AppendLine("using AbstUI.Primitives;");
        sb.AppendLine();
        var className = Sanitize(cast.Name) + "Cast";
        sb.AppendLine($"public class {className} : IBlingoCastLibBuilder");
        sb.AppendLine("{");
        sb.AppendLine("    public async Task BuildAsync(IBlingoCastLibsContainer castLibs)");
        sb.AppendLine("    {");
        sb.AppendLine($"        var cast = castLibs.AddCast({FormatValue(cast.Name)}, false);");
        var idx = 0;
        foreach (var m in cast.Members)
        {
            idx++;
            sb.Append(GenerateMember(m, idx));
        }
        sb.AppendLine("    }");
        sb.AppendLine("}");
        return sb.ToString();
    }

    protected virtual string GenerateMember(BlingoMemberDTO dto, int idx)
    {
        var sb = new StringBuilder();
        var typeName = Enum.GetName(typeof(BlingoMemberTypeDTO), dto.Type) ?? "Unknown";
        sb.AppendLine($"        var member{idx} = ({MemberClass(dto.Type)})cast.Add(BlingoMemberType.{typeName}, {dto.NumberInCast}, {FormatValue(dto.Name)}, {FormatValue(dto.FileName)}, new APoint({CleanFloat(dto.RegPoint.X)}, {CleanFloat(dto.RegPoint.Y)}));");
        foreach (var prop in dto.GetType().GetProperties())
        {
            if (prop.Name == "MarkDownText")
            {
                //member1.MarkDownText = @"Local 2 0
                var valuetxt = prop.GetValue(dto);
                if (valuetxt == null || IsDefaultValue(valuetxt))
                    continue;
                sb.AppendLine($"        member{idx}.SetTextMD({FormatValue(valuetxt)});");
                continue;
            }
            if (SkipMemberProperty(prop.Name))
                continue;
            var value = prop.GetValue(dto);
            if (value == null || IsDefaultValue(value))
                continue;
            var formattedValue = FormatValue(value);
            if (formattedValue != "null")
                sb.AppendLine($"        member{idx}.{prop.Name} = {formattedValue};");
        }
        return sb.ToString();
    }
    private static string CleanFloat(float value) => value.ToString().Replace(',', '.')+"f";
    public virtual string GenerateScoreClass(BlingoMovieDTO movie)
    {
        var sb = new StringBuilder();
        sb.Append(_header);
        sb.AppendLine("using BlingoEngine.Core;");
        sb.AppendLine("using BlingoEngine.Movies;");
        sb.AppendLine("using BlingoEngine.Sprites;");
        sb.AppendLine("using BlingoEngine.Members;");
        sb.AppendLine("using AbstUI.Primitives;");
        sb.AppendLine();
        sb.AppendLine("public class ScoreBuilder : IBlingoScoreBuilder");
        sb.AppendLine("{");
        sb.AppendLine("    public async Task BuildAsync(IBlingoMovie movie)");
        sb.AppendLine("    {");
        foreach (var sp in movie.Sprite2Ds)
        {
            var memberRef = sp.Member;
            var member = memberRef != null
                ? movie.Casts.SelectMany(c => c.Members)
                    .FirstOrDefault(m => m.CastLibNum == memberRef.CastLibNum && m.NumberInCast == memberRef.MemberNum)
                : null;
            var castNum = member?.CastLibNum ?? memberRef?.CastLibNum ?? 0;
            var memberNum = member?.NumberInCast ?? memberRef?.MemberNum ?? 0;
            sb.AppendLine($"        movie.AddSprite({sp.SpriteNum}, {sp.BeginFrame}, {sp.EndFrame}, {FormatFloat(sp.LocH)}, {FormatFloat(sp.LocV)}, s =>");
            sb.AppendLine("        {");
            var props = new List<string>();
            void AddProp(object? val, string assignment)
            {
                if (!IsDefaultValue(val) && val != null)
                    props.Add(assignment);
            }

            AddProp(sp.Name, $"s.Name = {FormatValue(sp.Name)};");
            AddProp(sp.DisplayMember, $"s.DisplayMember = {FormatValue(sp.DisplayMember)};");
            if (sp.Skew != 0) AddProp(sp.SpritePropertiesOffset, $"s.SpritePropertiesOffset = {FormatValue(sp.SpritePropertiesOffset)};");
            if (sp.Lock) AddProp(sp.Lock, $"s.Lock = {FormatValue(sp.Lock)};");
            AddProp(sp.Visibility, $"s.Visibility = {FormatValue(sp.Visibility)};");
            if (sp.LocZ != 0 && sp.LocZ != sp.SpriteNum)AddProp(sp.LocZ, $"s.LocZ = {FormatValue(sp.LocZ)};");
            if (sp.Rotation != 0) AddProp(sp.Rotation, $"s.Rotation = {FormatValue(sp.Rotation)};");
            if (sp.Skew != 0) AddProp(sp.Skew, $"s.Skew = {FormatValue(sp.Skew)};");
            if (sp.Ink != 0) AddProp(sp.Ink, $"s.Ink = {FormatValue(sp.Ink)};");
            if (sp.ForeColor.ToAColor() != AColors.Black) AddProp(sp.ForeColor, $"s.ForeColor = {FormatValue(sp.ForeColor)};");
            if (sp.BackColor.ToAColor() != AColors.White) AddProp(sp.BackColor, $"s.BackColor = {FormatValue(sp.BackColor)};");
            if (sp.Blend != 100f) AddProp(sp.Blend, $"s.Blend = {FormatValue(sp.Blend)};");
            if (sp.Editable) AddProp(sp.Editable, $"s.Editable = {FormatValue(sp.Editable)};");
            if (sp.FlipH) AddProp(sp.FlipH, $"s.FlipH = {FormatValue(sp.FlipH)};");
            if (sp.FlipV) AddProp(sp.FlipV, $"s.FlipV = {FormatValue(sp.FlipV)};");
            AddProp(sp.ScoreColor, $"s.ScoreColor = {FormatValue(sp.ScoreColor)};");
            if (sp.Width != 0) AddProp(sp.Width, $"s.Width = {FormatValue(sp.Width)};");
            if (sp.Height != 0) AddProp(sp.Height, $"s.Height = {FormatValue(sp.Height)};");
            props.Add($"s.Member = movie.CastLib.GetMember({memberNum}, {castNum});");

            sb.AppendLine($"            {string.Join(" ", props)}");
            sb.AppendLine("        });");
        }
        sb.AppendLine("    }");
        sb.AppendLine("}");
        return sb.ToString();
    }

    public virtual string GenerateMovieClass(BlingoMovieDTO movie)
    {
        var sb = new StringBuilder();
        sb.Append(_header);
        sb.AppendLine("using BlingoEngine.Core;");
        sb.AppendLine("using BlingoEngine.Movies;");
        sb.AppendLine();
        sb.AppendLine("public class MovieBuilder : IBlingoMovieBuilder");
        sb.AppendLine("{");
        sb.AppendLine("    public async Task<IBlingoMovie> BuildAsync(IBlingoPlayer player)");
        sb.AppendLine("    {");
        if (movie.Casts.Any()) {
            foreach (var cast in movie.Casts)
                sb.AppendLine($"        await player.LoadAsync<{Sanitize(cast.Name)}Cast>();");
        }
        sb.AppendLine($"        var movie = player.NewMovie({FormatValue(movie.Name)});");
        sb.AppendLine($"        movie.Tempo = {movie.Tempo};");
        if (!string.IsNullOrWhiteSpace(movie.About))
            sb.AppendLine($"        movie.About = {FormatValue(movie.About)};");
        if (!string.IsNullOrWhiteSpace(movie.Copyright))
            sb.AppendLine($"        movie.Copyright = {FormatValue(movie.Copyright)};");
        if (!string.IsNullOrWhiteSpace(movie.UserName))
            sb.AppendLine($"        movie.UserName = {FormatValue(movie.UserName)};");
        if (!string.IsNullOrWhiteSpace(movie.CompanyName))
            sb.AppendLine($"        movie.CompanyName = {FormatValue(movie.CompanyName)};");
        sb.AppendLine("        await new ScoreBuilder().BuildAsync(movie);");
        sb.AppendLine("        return movie;");
        sb.AppendLine("    }");
        sb.AppendLine("}");
        return sb.ToString();
    }

    public virtual string MemberClass(BlingoMemberTypeDTO type) => type switch
    {
        BlingoMemberTypeDTO.Bitmap => nameof(BlingoMemberBitmap),
        BlingoMemberTypeDTO.Sound => nameof(BlingoMemberSound),
        BlingoMemberTypeDTO.FilmLoop => nameof(BlingoFilmLoopMember),
        BlingoMemberTypeDTO.Text => nameof(BlingoMemberText),
        BlingoMemberTypeDTO.Field => nameof(BlingoMemberField),
        BlingoMemberTypeDTO.Shape => nameof(BlingoMemberShape),
        BlingoMemberTypeDTO.Script => nameof(BlingoMemberScript),
        BlingoMemberTypeDTO.Palette => nameof(BlingoColorPaletteMember),
        BlingoMemberTypeDTO.Transition => nameof(BlingoTransitionMember),
        _ => nameof(BlingoMember)
    };

    protected virtual bool SkipMemberProperty(string name) => name switch
    {
        "Name" or "Number" or "CastLibNum" or "NumberInCast" or "Type" or "RegPoint" or "FileName" or
        "ImageFile" or "SoundFile" or "Bytes" or "Size" or "Text" or "Length" or "Stereo" or "FrameCount" => true,
        _ => false
    };


    protected virtual bool IsDefaultValue(object? value)
    {
        if (value == null)
            return true;
        if (value is string s)
            return string.IsNullOrEmpty(s);
        var type = value.GetType();
        if (!type.IsValueType)
            return false;
        return value.Equals(System.Activator.CreateInstance(type));
    }


    protected virtual string FormatValue(object value)
    {
        switch (value)
        {
            case string s:
                return "@\"" + s.Replace("\"", "\"\"") + "\"";
            case bool b:
                return b ? "true" : "false";
            case float f:
                return f.ToString(CultureInfo.InvariantCulture) + "f";
            case double d:
                return d.ToString(CultureInfo.InvariantCulture) + "d";
            case int or long or short:
                return Convert.ToString(value, CultureInfo.InvariantCulture) ?? "0";
            case BlingoPointDTO p:
                return $"new APoint({p.X}, {p.Y})";
            case BlingoColorDTO c:
                return $"new AColor({c.R}, {c.G}, {c.B}, {c.A})";
            default:
                return "null";
        }
    }
    protected virtual string FormatFloat(float value) => value.ToString(CultureInfo.InvariantCulture) + "f";

    protected virtual string Sanitize(string name)
    {
        var sb = new StringBuilder();
        foreach (var ch in name)
            sb.Append(char.IsLetterOrDigit(ch) ? ch : '_');
        if (sb.Length == 0 || char.IsDigit(sb[0]))
            sb.Insert(0, '_');
        return sb.ToString();
    }
}


